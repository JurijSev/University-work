public class Time {
    int second;
    int minute;
    int hour;

    public Time(){
        this.second = 0;
        this.minute = 0;
        this.hour = 0;
    }

    public int getSecond(){
        return this.second;
    }

    public int getMinute(){
        return this.minute;
    }

    public int getHour(){
        return this.hour;
    }
}
